    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String id = jTextField1.getText().trim();

    // Validation
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Please enter a student ID.");
        return;
    }

    String status;
    if (jCheckBox1.isSelected()) {
        status = "Present";
    } else if (jCheckBox2.isSelected()) {
        status = "Absent";
    } else {
        JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Please select Present or Absent.");
        return;
    }

    try {
      
   
       String sql = "INSERT INTO attendance (student_id, status) VALUES (?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setInt(1, Integer.parseInt(id));
        stmt.setString(2, status);
        stmt.executeUpdate();

        JOptionPane.showMessageDialog(this, "‚úÖ Attendance submitted!");

        // Reset form
        jTextField1.setText("");
        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "‚ùå Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

